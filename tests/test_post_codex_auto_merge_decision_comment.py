def test_sync_guard_decision_labels_removes_auto_merge_and_adds_human_review_for_high_risk() -> None:
    calls: list[tuple[str, str, dict[str, list[str]] | None]] = []
    decision = sample_decision() | {
        "auto_merge_label": "custom-auto-ok",
        "human_review_label": "custom-human-review",
    }

    def fake_request(method: str, url: str, token: str, payload=None):
        calls.append((method, url, payload))
        assert token == "token"
        if method == "DELETE":
            assert url.endswith("/issues/12/labels/custom-auto-ok")
            return None
        if method == "POST":
            assert url.endswith("/issues/12/labels")
            assert payload == {"labels": ["custom-human-review"]}
            return {"labels": [{"name": "custom-human-review"}]}
        raise AssertionError((method, url))

    actions = sync_guard_decision_labels(
        api_url="https://api.github.local",
        repo="owner/repo",
        token="token",
        pr_number=12,
        decision=decision,
        request_fn=fake_request,
    )

    assert actions == [
        "Removed stale auto-merge label `custom-auto-ok` from PR #12.",
        "Ensured human-review label `custom-human-review` on PR #12.",
    ]
    assert [method for method, _, _ in calls] == ["DELETE", "POST"]


