def test_cli_updates_comment_with_label_hygiene_actions(tmp_path: Path, monkeypatch) -> None:
    pr_path = tmp_path / "pr.json"
    decision_path = tmp_path / "decision.json"
    output = tmp_path / "comment.md"
    pr_path.write_text(json.dumps(sample_pr()), encoding="utf-8")
    decision_path.write_text(json.dumps(sample_decision()), encoding="utf-8")
    calls: list[tuple[str, str, dict[str, Any] | None]] = []
    patched_comment_body = ""

    def fake_request(method: str, url: str, token: str, payload=None):
        nonlocal patched_comment_body
        calls.append((method, url, payload))
        assert token == "token"
        if method == "GET" and "/comments" in url:
            if len([item for item in calls if item[0] == "GET" and "/comments" in item[1]]) == 1:
                return []
            return [{"id": 99, "body": f"{guard_marker(12)}\nold"}]
        if method == "POST" and url.endswith("/issues/34/comments"):
            return {"html_url": "https://github.local/comment/99"}
        if method == "DELETE":
            return None
        if method == "POST" and url.endswith("/issues/12/labels"):
            return {"labels": [{"name": "human-review-required"}]}
        if method == "PATCH":
            patched_comment_body = payload["body"]
            return {"html_url": "https://github.local/comment/99"}
        raise AssertionError((method, url, payload))

    monkeypatch.setenv("GITHUB_TOKEN", "token")
    monkeypatch.setattr(guard_comment, "github_request", fake_request)
    monkeypatch.setattr(
        sys,
        "argv",
        [
            "post_codex_auto_merge_decision_comment.py",
            "--repo",
            "owner/repo",
            "--pr-json",
            str(pr_path),
            "--decision-json",
            str(decision_path),
            "--output-file",
            str(output),
            "--sync-labels",
            "--api-url",
            "https://api.github.local",
        ],
    )

    assert guard_comment.main() == 0
    assert "### Label hygiene" in patched_comment_body
    assert "Removed stale auto-merge label `auto-merge-ok`" in patched_comment_body
    assert "Ensured human-review label `human-review-required`" in patched_comment_body
    assert output.read_text(encoding="utf-8") == patched_comment_body


