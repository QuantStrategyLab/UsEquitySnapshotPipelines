def test_extract_monthly_issue_number_uses_decision_marker_prefix() -> None:
    pr = sample_pr()
    decision = {"monthly_marker_prefix": "<!-- custom-marker:issue-"}
    pr["body"] = "<!-- custom-marker:issue-77 -->"

    assert extract_monthly_issue_number(pr, decision) == 77
    assert extract_monthly_issue_number({"body": "no marker"}, decision) is None


